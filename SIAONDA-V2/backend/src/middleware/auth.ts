import { Request, Response, NextFunction } from 'express';
import jwt from 'jsonwebtoken';
import { AppError } from './errorHandler';
import { prisma } from '../config/database';

export interface AuthRequest extends Request {
  usuario?: {
    id: number;
    nombre: string;
    tipo: string;
    codigo: string;
  };
}

export const authenticate = async (
  req: AuthRequest,
  res: Response,
  next: NextFunction
) => {
  try {
    const authHeader = req.headers.authorization;

    if (!authHeader || !authHeader.startsWith('Bearer ')) {
      throw new AppError('Token no proporcionado', 401);
    }

    const token = authHeader.substring(7);
    const secret = process.env.JWT_SECRET;

    if (!secret) {
      throw new AppError('Configuraci칩n de JWT inv치lida', 500);
    }

    const decoded = jwt.verify(token, secret) as {
      id: number;
      nombre: string;
      tipo: string;
      codigo: string;
    };

    // Verificar que el usuario sigue activo
    const usuario = await prisma.usuario.findUnique({
      where: { id: decoded.id },
      include: {
        tipo: true,
        estado: true
      }
    });

    if (!usuario || usuario.estado.nombre !== 'Activo') {
      throw new AppError('Usuario no autorizado', 401);
    }

    req.usuario = {
      id: decoded.id,
      nombre: decoded.nombre,
      tipo: usuario.tipo.nombre,
      codigo: decoded.codigo
    };

    next();
  } catch (error) {
    if (error instanceof jwt.JsonWebTokenError) {
      next(new AppError('Token inv치lido', 401));
    } else {
      next(error);
    }
  }
};

export const authorize = (...roles: string[]) => {
  return (req: AuthRequest, res: Response, next: NextFunction) => {
    if (!req.usuario) {
      return next(new AppError('No autenticado', 401));
    }

    if (!roles.includes(req.usuario.tipo)) {
      return next(new AppError('No tiene permisos para esta acci칩n', 403));
    }

    next();
  };
};
